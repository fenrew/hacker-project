(function($) { 
    'use strict';
    
    $.fn.cheatCodeVi = function(options) {
        var timer,
            input = '',
            settings = $.extend({ 
                code: 7173808389, // GIPSY // vivian
                time: 500,
                callback: function() {var playerPiece = '<span class="player"><img src="pics/v.png" alt="" height="70px" ></span>'; // 
                $(".game").append(playerPiece);
                refreshBoard();
            
            }
            }, options);
        
        function checkInput() {
            if (input === settings.code.toString()) settings.callback();
        }
        
        this.keyup(function(e) {
            input += e.which; 
            clearTimeout(timer);
            timer = setTimeout(function() { input = ''; }, settings.time); 
            checkInput();
        });
        
        return this; 
    }
    
}(jQuery));

